package basic;

public class switchdemo {
public static void main(String[] args) {
	int tech=3;
	switch(tech)
	{
	case 1:{
		System.out.println("java");
		break;
	}
	case 2:{
		System.out.println("python");
		break;
	}
	case 3:{
		System.out.println("c");
		break;
	}
	default:{
		System.out.println("c++");
	}
	}
}
}
